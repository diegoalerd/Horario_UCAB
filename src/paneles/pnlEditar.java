    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaSelect = tablaMaterias.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel) tablaMaterias.getModel();
        if(filaSelect >= 0){
                 int confirmacion = JOptionPane.showOptionDialog(null, "¿Desea realizar la operación?", "::::...CONFIRMACIÓN...:::",
                 JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
                 if (confirmacion == 0){
                    XML.borrarMateria(tablaMaterias.getValueAt(filaSelect, 0).toString(), Integer.parseInt((String) tablaMaterias.getValueAt(filaSelect, 3)));
                 }

            modelo.removeRow(filaSelect);}
        else
            JOptionPane.showMessageDialog(this, "Por favor seleccione una fila.");
    }//GEN-LAST:event_btnEliminarActionPerformed

