    private void llenarTabla(controlXML datosXML){
        String[] columna = {"Materia", "Profesor", "Carrera", "NRC"};
        DefaultTableModel dtm = new DefaultTableModel(null, columna);
        ArrayList<Materia> datosP = datosXML.listaMaterias();
        for(Materia M : datosP){
                String[] row = {M.getMateria(), M.getProfesor(), M.getCarrera(), Integer.toString(M.getNrc())};
                dtm.addRow(row);
        }
        tablaMaterias.setModel(dtm);
    }
    

